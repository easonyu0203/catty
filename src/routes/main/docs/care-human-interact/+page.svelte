			<div class="w-[36rem] space-y-3">
				<h3 class="h3">第一步</h3>
				<p>
					第一步是設置貓咪專屬空間，建立安全感。這包括選擇一個安靜的空間，設置躲藏處，提供食物、水和貓砂等資源。
					在這個階段，重要的是不要與貓咪互動，讓他們感到安全。完成本步驟的指標是貓咪願意主動出現。
				</p>
			</div>

			<div class="w-[36rem] space-y-3">
				<h3 class="h3">第二步</h3>
				<p>
					第二步是拉近距離，進行距離的減敏與反制約。在這個步驟中，準備一些獎勵零食，並在貓咪主動靠近時給予獎勵。
					從最遠的距離開始，逐漸拉近與貓咪的距離，讓他們逐漸習慣接近人類。這需要一定的耐心和時間，每次練習時間不要太長，每天可以進行多次。
				</p>
			</div>

			<div class="w-[36rem] space-y-3">
				<h3 class="h3">第三步</h3>
				<p>
					第三步是觸碰身體，進行觸碰的減敏與反制約。同樣地，使用獎勵零食，當貓咪逐漸接受觸碰時給予獎勵。
					這一步的目標是讓貓咪對於觸摸產生正向的反應，並逐漸讓他們與人類有更親密的接觸。
				</p>
			</div>

			<div class="w-[36rem] space-y-3">
				<h3 class="h3">總結</h3>
				<p>
					保持環境的安全感、溫和且可預期的互動以及給予貓咪獎勵當他們主動靠近的重要性，這可以幫助維持人貓關係並讓貓咪持續更親人。
					總結來說，野貓親人訓練是一個逐步的過程，需要耐心和時間。透過設置專屬空間、拉近距離和觸碰身體的步驟，我們可以幫助新貓咪變得更親人並建立幸福平衡的生活。
				</p>
