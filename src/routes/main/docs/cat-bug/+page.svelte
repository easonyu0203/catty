				<h3 class="h3">心絲蟲</h3>
				<p>
					貓咪心絲蟲傳播通過蚊子，感染風險低，但仍有少數貓咪可能受到影響。體型小的貓只需1至2隻心絲蟲即可威脅成年貓。發病前沒明顯徵兆，飼主難以觀察。例行驅蟲是重要預防措施之一。
				</p>
			</div>

			<div class="w-[36rem] space-y-3">
				<h3 class="h3">條蟲</h3>
				<p>
					貓咪感染絛蟲常與跳蚤一起處理，因跳蚤是其傳播途徑之一。
					感染條蟲的貓咪常表現營養不良、無光澤毛髮、食慾不振、嘔吐、腹瀉和慢性腸炎等症狀。治療通常包括口服或注射藥物，但也需處理跳蚤問題，以防再次感染。寄生蟲防治須綜合處理跳蚤和絛蟲問題。
				</p>
			</div>

			<div class="w-[36rem] space-y-3">
				<h3 class="h3">蛔蟲</h3>
				<p>
					蛔蟲是高度傳染性的寄生蟲，可感染貓咪、狗狗和人類，主要透過糞便傳染。
					母貓和幼貓需特別注意，因幼貓可能通過母乳感染。蛔蟲寄生於腸道，造成營養不良、食慾不振、體虛、嘔吐和腹脹等症狀。蛔蟲治療通常簡單，定期檢查和服用驅蟲藥可有效治療感染。
				</p>
			</div>

			<div class="w-[36rem] space-y-3">
				<h3 class="h3">肺蟲</h3>
				<p>
					肺蟲是經由口糞感染，蝸牛和蛞蝓通常是中間宿主。成蟲寄生在貓咪的肺部，引起咳嗽和食慾不振等症狀；嚴重情況下可能導致呼吸困難和支氣管肺炎。
				</p>
			</div>

			<div class="w-[36rem] space-y-3">
				<h3 class="h3">鉤蟲</h3>
				<p>
					鉤蟲通常透過貓食入帶有蟲卵的物質感染，或者透過皮膚滲透侵入貓咪體內。它是一種高感染性的寄生蟲，但通常只有戶外貓或接觸戶外環境的貓比較容易感染。
					鉤蟲寄生在貓咪的腸壁並吸食血液，導致貧血、營養不良，嚴重時可能出現拉血和死亡。幼貓和老貓抵抗力較差，更容易受到嚴重感染，因此建議定期制定驅蟲計劃預防感染。
				</p>
			</div>

			<div class="w-[36rem] space-y-3">
				<h3 class="h3">耳疥蟲</h3>
				<p>
					耳疥蟲是一種屬於蟎類的寄生蟲，專門寄生在貓咪的耳道內。它們喜歡耳道溫暖潮濕的環境，並在其中產卵。
					耳疥蟲以耳道內的粘膜為食，寄生的貓咪通常會因為耳朵瘙癢而不停抓撓。耳疥蟲主要透過接觸感染途徑傳播，也就是當貓咪接觸到已被寄生的貓咪時才會傳染。
				</p>
			</div>

			<div class="w-[36rem] space-y-3">
				<h3 class="h3">治療及預防</h3>
				<p>
					為了保護戶外貓咪免受寄生蟲的傳染，需要按時進行驅蟲計劃和措施。保持居家環境清潔，防止老鼠、跳蚤和蚊子等常見寄生蟲滋生。攝取益生菌維護腸道健康，減少寄生蟲對腸道的危害。
					定期帶寵物進行健康檢查，預防醫學觀念至關重要。市面上有安全可靠的預防藥物，可在獸醫院購買，根據獸醫指導使用。這些措施能有效預防常見寄生蟲的問題。
