			<div class="w-[36rem] space-y-3">
				<h3 class="h3">基礎問診</h3>
				<p>
					基礎問診是指詢問貓咪的相關病史、飲食狀況、排便和尿液狀況、驅蟲藥使用、疫苗接種和居住環境等信息。透過這些問診，獸醫師可以更深入地了解貓咪的日常生活細節。
					這種深入的了解能夠讓獸醫師更及時地發現可能存在的潛在問題。這些資訊提供了一個更完整的圖像，幫助獸醫師進行全面的評估，並提供更適當的治療和建議。
				</p>
			</div>

			<div class="w-[36rem] space-y-3">
				<h3 class="h3">理學檢查</h3>
				<p>
					理學檢查是指獸醫師利用感官進行直接觀察、觸診和聽診等操作，以獲得貓咪的身體資訊。
					透過使用聽診器、檢耳鏡、肛溫計等輔助工具進行非侵入性檢查，可以初步了解和發現貓咪身體上的一些異常現象。
					這些檢查結果能夠作為臨床診斷的參考，幫助獸醫師進一步評估貓咪的健康狀況。
				</p>
			</div>

			<div class="w-[36rem] space-y-3">
				<h3 class="h3">血液生化檢驗</h3>
				<p>
					血液生化檢驗提供了貓咪身體狀態的數值化資訊，包括腎臟相關指數、肝臟相關指數、血糖數值、血球分布等，幫助獸醫師評估貓咪整體健康狀態。
				</p>
			</div>

			<div class="w-[36rem] space-y-3">
				<h3 class="h3">尿檢與糞檢</h3>
				<p>
					尿液檢驗可以檢測異常成分，例如蛋白質、血液或結晶。它提供重要的訊息，如泌尿道問題、腎臟疾病和糖尿病等是否存在。
					這些情況通常可以從尿液中顯示出異常。另一方面，糞便檢查可以了解貓咪的腸胃消化狀況，並檢測是否有腸胃寄生蟲感染等。這些檢查有助於獸醫師全面評估貓咪的健康狀況。
				</p>
			</div>

			<div class="w-[36rem] space-y-3">
				<h3 class="h3">X光影像檢查</h3>
				<p>
					X光檢查觀察貓咪腹腔、胸腔情況，包括骨骼、器官位置、癌細胞轉移、懷孕及泌尿道結石。需多角度拍攝，避免誤判，提供詳細圖像資訊，協助準確診斷。
				</p>
			</div>

			<div class="w-[36rem] space-y-3">
				<h3 class="h3">超音波影像檢查</h3>
				<p>
					心臟超音波和腹部超音波檢查評估貓咪心臟結構、功能缺失，以及肝臟、膽囊、腸胃、腎臟等器官的影像，檢測肥大、瓣膜問題、腫瘤、結石或器官疾病。
				</p>
